
public class Bitwise {
	
	public static void main(String args[]) {
		int a =9;
		int b=1;
		
		System.out.println("And "+(a & b)); //1
		
		System.out.println("OR"+(a | b)); //9
		
		System.out.println("XOR"+(a ^ b)); //8
		
		System.out.println("Complement"+ ~a); //6
		
		
		
	}

}
